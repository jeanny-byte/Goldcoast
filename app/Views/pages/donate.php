// JavaScript logic for slider and emoji color change
const slider = document.getElementById('amount-slider');
const amountDisplay = document.getElementById('amount-display');
const emojis = document.querySelectorAll('.emoji-display span');
const currency = document.getElementById('currency-select');
let emojiCount = 0;

function recalcEmojiCount() {
  const value = parseInt(slider.value, 10);
  amountDisplay.value = (currency.value === 'USD' ? '$' : 'â‚µ') + value;
  if (currency.value === 'USD') {
    emojiCount = Math.floor(value / 1000);
  } else if (currency.value === 'GHS'){
    emojiCount = Math.floor(value / 15000);
  }
  emojis.forEach((emoji, index) => {
    emoji.style.color = index < emojiCount ? 'green' : 'gray';
  });
}

slider.addEventListener('input', recalcEmojiCount);
currency.addEventListener('change', recalcEmojiCount);
recalcEmojiCount();
</script>
